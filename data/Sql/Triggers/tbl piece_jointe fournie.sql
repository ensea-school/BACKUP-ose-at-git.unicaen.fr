CREATE OR REPLACE TRIGGER T_PJF_PIECE_JOINTE_FICHER
AFTER INSERT 
OR UPDATE OF 
  piece_jointe_id,
	fichier_id
OR DELETE ON PIECE_JOINTE_FICHIER
FOR EACH ROW
BEGIN

  FOR p IN (

    SELECT DISTINCT
      pj.intervenant_id
    FROM
      piece_jointe pj
    WHERE
         pj.id = :NEW.piece_jointe_id
      OR pj.id = :OLD.piece_jointe_id

  ) LOOP

    OSE_EVENT.DEMANDE_CALCUL( OSE_PIECE_JOINTE_FOURNIE.package_sujet, p.intervenant_id );

  END LOOP;

END;

/

CREATE OR REPLACE TRIGGER T_PJF_FICHER
AFTER INSERT 
OR UPDATE OF 
  histo_creation,
	histo_destruction
OR DELETE ON FICHIER
FOR EACH ROW
BEGIN

  FOR p IN (

    SELECT DISTINCT
      pj.intervenant_id
    FROM
      piece_jointe pj
      JOIN piece_jointe_fichier pjf ON pjf.piece_jointe_id = pj.id
    WHERE
         pjf.fichier_id = :NEW.id
      OR pjf.fichier_id = :OLD.id

  ) LOOP

    OSE_EVENT.DEMANDE_CALCUL( OSE_PIECE_JOINTE_FOURNIE.package_sujet, p.intervenant_id );

  END LOOP;

END;

/

