CREATE OR REPLACE TRIGGER T_PJ_PJ_FOURNIE
AFTER INSERT 
OR UPDATE OF 
  annee_id,
	type_piece_jointe_id,
	intervenant_id,
	validation_id,
	fichier_id
OR DELETE ON TBL_PIECE_JOINTE_FOURNIE
FOR EACH ROW
BEGIN
  IF NOT UNICAEN_TBL.GET_ACTIF THEN RETURN; END IF;

  IF :NEW.intervenant_id IS NOT NULL THEN
    UNICAEN_TBL.DEMANDE_CALCUL( 'piece_jointe', UNICAEN_TBL.make_params('intervenant_id', :NEW.intervenant_id ) );
  END IF;
  
  IF :OLD.intervenant_id IS NOT NULL THEN
    UNICAEN_TBL.DEMANDE_CALCUL( 'piece_jointe', UNICAEN_TBL.make_params('intervenant_id', :OLD.intervenant_id ) );
  END IF;

END;

/

