CREATE OR REPLACE TRIGGER T_DOS_INTERVENANT
AFTER INSERT 
OR UPDATE OF 
  annee_id,
	histo_creation,
	histo_destruction
OR DELETE ON INTERVENANT
FOR EACH ROW
BEGIN

  IF :NEW.id IS NOT NULL THEN
    OSE_DOSSIER.DEMANDE_CALCUL( :NEW.id );
  END IF;
  
  IF :OLD.id IS NOT NULL THEN
    OSE_DOSSIER.DEMANDE_CALCUL( :OLD.id );
  END IF;

END;

/

CREATE OR REPLACE TRIGGER T_DOS_STATUT_INTERVENANT
AFTER INSERT 
OR UPDATE OF 
    peut_saisir_dossier
OR DELETE ON STATUT_INTERVENANT
FOR EACH ROW
BEGIN

  FOR p IN (
  
    SELECT DISTINCT
      i.id intervenant_id
    FROM
      intervenant i
    WHERE
         i.statut_id = :NEW.id
      OR i.statut_id = :OLD.id
  
  ) LOOP
  
    OSE_DOSSIER.DEMANDE_CALCUL( p.intervenant_id );
  
  END LOOP;

END;

/

